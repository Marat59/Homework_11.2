def upper_sentence(sentence):
    """Функция возвращает все буквы предложения заглавными."""
    return sentence.upper()

sentence = "Hello world"

print(upper_sentence(sentence))

def title_sentence(sentence):
    """Функция возвращает первые буквы слов заглавными."""
    return sentence.title()

sentence = "hello world"

print(title_sentence(sentence))
